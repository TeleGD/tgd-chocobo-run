package fr.tgd.world;


public class Wall {
	private double inf,supp;
	

	public Wall(double inf , double supp) {
		super();
		this.inf = inf;
		this.supp = supp;
	}

	public double getInf() {
		return inf;
	}


	public void setInf(double inf) {
		this.inf = inf;
	}


	public double getSupp() {
		return supp;
	}

	public void setSupp(double supp) {
		this.supp = supp;
	}
	
	
}


